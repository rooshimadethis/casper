struct TelemetrySanitizer {
    /// Scans the input string for Private Use Area (PUA) Unicode characters
    /// (such as custom icon glyphs) and replaces them with a descriptive text label
    /// (e.g., [Icon: add] or [Icon: U+XXXX]).
    static func sanitize(_ input: String) -> String {
        var result = ""
        for char in input {
            if let scalar = char.unicodeScalars.first, isPrivateUseArea(scalar.value) {
                if let name = codicons[char] {
                    result.append("[Icon: \(name)]")
                } else {
                    let hex = String(scalar.value, radix: 16, uppercase: true)
                    result.append("[Icon: U+\(hex)]")
                }
            } else {
                result.append(char)
            }
        }
        return result
    }
    
    private static func isPrivateUseArea(_ value: UInt32) -> Bool {
        return (0xE000...0xF8FF).contains(value) ||
               (0xF0000...0xFFFFD).contains(value) ||
               (0x100000...0x10FFFD).contains(value)
    }

